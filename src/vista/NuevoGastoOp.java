    private void nuevoGasto(){
        String activoFinanciero="";
        activoFinanciero=txt_Cuenta.getSelectedItem().toString();
        if(!txtTipoDeGasto.getText().isEmpty()||!txtDescripcion.getText().isEmpty()||!txtCoste.getText().isEmpty()||!txtFechaInicio.getText().isEmpty()||!activoFinanciero.equals("")){
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");
            CGasto aux=new CGasto();
            Gasto gasto=new Gasto(txtTipoDeGasto.getText(),txtDescripcion.getText(),Double.parseDouble(txtCoste.getText()),LocalDate.parse(txtFechaInicio.getText(), formatter),LocalDate.parse(txtFechaFin.getText(), formatter),aux.claveCuenta(activoFinanciero),FrmMenu.pers.getDni());
            Cuenta cuenta=cuentaDescripcion(activoFinanciero);
            if(cuenta.getDinero()<Double.parseDouble(txtCoste.getText())){
                JOptionPane.showMessageDialog(null, "Saldo insuficiente en la cuenta" ); 
            }else{
                if(!aux.ingresarGasto(gasto)){
                   JOptionPane.showMessageDialog(null, "Fallo al crear el Gasto " );
                }else{
                    JOptionPane.showMessageDialog(null, "Se ha creado un gasto");
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Complete los campos");
        }
    }
